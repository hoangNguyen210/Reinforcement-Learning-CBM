def _load_mmbench_video_tsv(data_path: str) -> List[Dict[str, Any]]:
    """Load MMBench-Video from TSV. video_path in TSV is relative (e.g. ./video/xxx.mp4).
    Use MMBENCH_VIDEO_ROOT, or default to the TSV directory.
    """
    video_root = os.environ.get("MMBENCH_VIDEO_ROOT", os.path.dirname(os.path.abspath(data_path)))
    samples: List[Dict[str, Any]] = []
    with open(data_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        for i, row in enumerate(reader):
            video_path_rel = str(row.get("video_path", "")).strip()
            if not video_path_rel:
                continue
            # video_path: ./video/wZxzBvAgqxc.mp4 -> basename = wZxzBvAgqxc.mp4
            video_basename = os.path.basename(video_path_rel)
            video_path_abs = os.path.join(video_root, video_basename)
            if not os.path.exists(video_path_abs):
                continue
            samples.append(
                {
                    "benchmark": "mmbench_video",
                    "sample_id": str(row.get("index", i)),
                    "index": int(row["index"]) if row.get("index", "").isdigit() else i,
                    "video": str(row.get("video", "")).strip(),
                    "video_path": video_path_abs,
                    "video_type": str(row.get("video_type", "")).strip(),
                    "question": str(row.get("question", "")).strip(),
                    "answer": str(row.get("answer", "")).strip(),
                    "dimensions": str(row.get("dimensions", "[]")).strip(),
                }
            )
    return samples


