def _resolve_mvbench_video_to_mp4(sample: Dict[str, Any]) -> str:
    """
    Resolve MVBench video path to mp4, handling gif/webm/frame formats and bound (start/end).
    Aligned with VLMEvalKit load_into_video_and_process.
    Results are cached in sample['_resolved_video_path'] to avoid repeated conversion.
    """
    if "_resolved_video_path" in sample:
        return sample["_resolved_video_path"]
    video_path = sample.get("video_path") or sample.get("video", "")
    if not video_path:
        raise KeyError("MVBench sample must contain video_path or video")
    data_type = sample.get("data_type", "video")
    bound = sample.get("bound", False)

    if data_type not in ("gif", "frame") and not video_path.lower().endswith(".webm"):
        processed = video_path
    else:
        try:
            from moviepy.editor import VideoFileClip, ImageSequenceClip
        except ImportError:
            raise ImportError(
                "MVBench gif/frame conversion requires moviepy. "
                "Install with: pip install moviepy==1.0.3"
            )
        if data_type == "gif" or os.path.splitext(video_path)[1].lower() == ".webm":
            processed = video_path.replace(os.path.splitext(video_path)[1], ".mp4")
            if not os.path.exists(processed):
                clip = VideoFileClip(video_path)
                clip.write_videofile(processed, codec="libx264")
                clip.close()
        elif data_type == "frame":
            processed = f"{video_path}.mp4"
            if not os.path.exists(processed):
                img_pattern = os.path.join(video_path, "*.jpg")
                image_files = sorted(glob.glob(img_pattern))
                if not image_files:
                    raise FileNotFoundError(f"No .jpg frames in {video_path}")
                clip = ImageSequenceClip(image_files, fps=3)
                clip.write_videofile(processed, codec="libx264")
                clip.close()
        else:
            processed = video_path

    if bound and sample.get("start") is not None and sample.get("end") is not None:
        try:
            from moviepy.editor import VideoFileClip
        except ImportError:
            raise ImportError("MVBench bound subclip requires moviepy.")
        base_name, suffix = os.path.splitext(processed)
        output_path = f"{base_name}_processed{suffix}"
        if not os.path.exists(output_path):
            video_clip = VideoFileClip(processed)
            end_val = min(sample["end"], video_clip.duration)
            clip = video_clip.subclip(sample["start"], end_val)
            clip.write_videofile(output_path)
            clip.close()
        sample["_resolved_video_path"] = output_path
        return output_path
    sample["_resolved_video_path"] = processed
    return processed


