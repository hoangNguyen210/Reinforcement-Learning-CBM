def _stage1_caption_single(
    samples: List[Dict[str, Any]], args: argparse.Namespace, log_prefix: str = "[Stage1-single]"
) -> List[Dict[str, Any]]:
    """Stage1: pre-frame + in-process vLLM."""
    from vllm import SamplingParams
    import time

    caption_sampling = SamplingParams(
        n=args.gen_num,
        temperature=args.caption_temperature,
        top_p=args.caption_top_p,
        top_k=20,
        max_tokens=args.caption_max_tokens,
        repetition_penalty=1.0,
        presence_penalty=1.5,
    )

    frame_root = get_frame_root(args)
    caption_llm = build_caption_model(args)
    caption_records: List[Dict[str, Any]] = []
    total_batches = (len(samples) + args.caption_batch_size - 1) // args.caption_batch_size if samples else 0
    start_time = time.time()
    for batch_idx, i in enumerate(range(0, len(samples), args.caption_batch_size), start=1):
        batch = samples[i : i + args.caption_batch_size]
        vllm_inputs: List[Dict[str, Any]] = []
        valid_idx: List[int] = []
        for j, s in enumerate(batch):
            video_path = ensure_video_path(s)
            video_id = get_video_id(s)
            try:
                frame_paths = save_video_frames_vlmevalkit(
                    video_path, video_id, frame_root, args.num_frames
                )
                vllm_inputs.append(build_frames_mm_input(frame_paths, benchmark=args.benchmark))
                valid_idx.append(j)
            except Exception as e:
                print(f"[WARN] skip video {video_path}: {e}")
        cap_out = caption_llm.generate(vllm_inputs, caption_sampling, use_tqdm=False) if vllm_inputs else []
        cap_map = {k: [o.text for o in req.outputs] for k, req in zip(valid_idx, cap_out)}
        for j, s in enumerate(batch):
            caption_records.append(
                {
                    "sample": s,
                    "captions": cap_map.get(j, ["[video_read_failed]"] * args.gen_num),
                }
            )
        if batch_idx == 1 or batch_idx % 5 == 0 or batch_idx == total_batches:
            _log_batch_progress(log_prefix, batch_idx, total_batches, start_time)
    return caption_records


def _stage2_answer_api(
    caption_records: List[Dict[str, Any]], args: argparse.Namespace, log_prefix: str = "[Stage2-API]"
) -> List[Dict[str, Any]]:
    """Stage2 via vLLM OpenAI API: text-only prompts (caption + question -> answer)."""
    import time
    from openai import AsyncOpenAI

    api_base = args.downstream_api_base.rstrip("/")
    model_name = getattr(args, "downstream_model_name", None) or os.path.basename(args.downstream_model_path.rstrip("/"))
    concurrency = int(getattr(args, "downstream_api_concurrency", 32))
    total_batches = (len(caption_records) + args.answer_batch_size - 1) // args.answer_batch_size if caption_records else 0

    async def _run_all_batches() -> List[Dict[str, Any]]:
        client = AsyncOpenAI(api_key="EMPTY", base_url=api_base)
        sem = asyncio.Semaphore(concurrency)

        async def _answer_one(prompt: str) -> str:
            async with sem:
                resp = await client.chat.completions.create(
                    model=model_name,
                    messages=[{"role": "user", "content": prompt}],
                    n=1,
                    temperature=args.answer_temperature,
                    max_tokens=args.answer_max_tokens,
                )
            return (resp.choices[0].message.content or "").strip()

        async def _run_prompts(prompts: List[str]) -> List[str]:
            tasks = [_answer_one(p) for p in prompts]
            return await asyncio.gather(*tasks, return_exceptions=True)

        final_records: List[Dict[str, Any]] = []
        start_time = time.time()
        for batch_idx, i in enumerate(range(0, len(caption_records), args.answer_batch_size), start=1):
            batch = caption_records[i : i + args.answer_batch_size]
            flat_prompts: List[str] = []
            sizes: List[int] = []
            for rec in batch:
                sample = rec["sample"]
                q = format_question_for_prompt(sample)
                caps = rec["captions"]
                sizes.append(len(caps))
                for cap in caps:
                    flat_prompts.append(build_downstream_prompt(sample, cap, q))
            results = await _run_prompts(flat_prompts)
            flat_resps = []
            for r in results:
                if isinstance(r, Exception):
                    flat_resps.append(f"[api_error:{type(r).__name__}]")
                else:
                    flat_resps.append(r)
            cursor = 0
            for rec, sz in zip(batch, sizes):
                responses = flat_resps[cursor : cursor + sz]
                cursor += sz
                final_records.append(
                    {"sample": rec["sample"], "captions": rec["captions"], "responses": responses}
                )
            if batch_idx == 1 or batch_idx % 5 == 0 or batch_idx == total_batches:
                _log_batch_progress(log_prefix, batch_idx, total_batches, start_time)
        return final_records

    return asyncio.run(_run_all_batches())


