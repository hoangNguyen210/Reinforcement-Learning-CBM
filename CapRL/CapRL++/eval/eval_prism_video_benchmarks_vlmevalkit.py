def _load_mvbench_metadata_index(base_dir: Optional[str] = None) -> Dict[Tuple[str, str], Dict[str, Any]]:
    meta: Dict[Tuple[str, str], Dict[str, Any]] = {}
    base_dir = base_dir or os.environ.get("MVBENCH_META_ROOT", "")
    if not os.path.isdir(base_dir):
        return meta
    for filename in os.listdir(base_dir):
        if not filename.endswith(".json"):
            continue
        task_type = _mvbench_filename_to_task_type(filename)
        with open(os.path.join(base_dir, filename), "r", encoding="utf-8") as f:
            rows = json.load(f)
        for row in rows:
            video_name = os.path.basename(str(row["video"]))
            candidates = [str(x) for x in row["candidates"]]
            answer_idx = next((i for i, x in enumerate(candidates) if x == row["answer"]), -1)
            answer_letter = chr(ord("A") + answer_idx) if answer_idx >= 0 else ""
            options = [f"{chr(ord('A') + i)}. {cand}" for i, cand in enumerate(candidates)]
            meta[(video_name, str(row["question"]).strip())] = {
                "task_type": task_type,
                "options": options,
                "answer_letter": answer_letter,
            }
    return meta


def _load_mvbench_tsv(data_path: str) -> List[Dict[str, Any]]:
    """Load MVBench from VLMEvalKit-style TSV (MVBench.tsv). Includes data_type, bound, prefix for non-mp4 handling."""
    data_root = os.path.dirname(os.path.abspath(data_path))
    data_root = os.environ.get("MVBench_ROOT", data_root)
    samples: List[Dict[str, Any]] = []
    with open(data_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        for i, row in enumerate(reader):
            prefix = str(row.get("prefix", "")).strip()
            video_rel = str(row.get("video", "")).strip()
            if not video_rel:
                continue
            video_path = os.path.join(data_root, prefix, video_rel)
            if not os.path.exists(video_path):
                continue
            candidates = _safe_literal_list(row.get("candidates", ""))
            options = [f"{chr(ord('A') + j)}. {c}" for j, c in enumerate(candidates)]
            answer_raw = str(row.get("answer", "")).strip()
            answer_idx = -1
            for j, c in enumerate(candidates):
                if c == answer_raw:
                    answer_idx = j
                    break
            answer = chr(ord("A") + answer_idx) if answer_idx >= 0 else answer_raw
            bound_val = row.get("bound", "False")
            bound = str(bound_val).lower() in ("true", "1", "yes")
            try:
                start = float(row["start"]) if bound and row.get("start") else None
                end = float(row["end"]) if bound and row.get("end") else None
            except (ValueError, TypeError):
                start, end = None, None
            samples.append(
                {
                    "benchmark": "mvbench",
                    "sample_id": f"mvbench_{i}",
                    "video_path": video_path,
                    "data_root": data_root,
                    "prefix": prefix,
                    "video": video_rel,
                    "data_type": str(row.get("data_type", "video")).strip().lower(),
                    "bound": bound,
                    "start": start,
                    "end": end,
                    "question": str(row.get("question", "")).strip(),
                    "options": options,
                    "answer": answer_raw,
                    "task_type": str(row.get("task_type", "Unknown")),
                }
            )
    return samples


def _load_mvbench_samples(data_path: str) -> List[Dict[str, Any]]:
    """Load MVBench from JSON/JSONL (legacy format). Assumes video_path points to mp4."""
    rows = load_json_or_jsonl(data_path)
    json_dir = os.path.join(os.path.dirname(os.path.abspath(data_path)), "json")
    meta_index = _load_mvbench_metadata_index(json_dir)
    samples: List[Dict[str, Any]] = []
    for i, row in enumerate(rows):
        video_path = row.get("video_path") or row.get("video")
        if not video_path:
            continue
        video_name = os.path.basename(video_path)
        question_text = str(row["question"]).strip()
        parsed_question, parsed_options = _parse_question_options_from_text(question_text)
        meta = meta_index.get((video_name, parsed_question), {})
        options = meta.get("options") or parsed_options
        answer = str(row.get("answer") or meta.get("answer_letter") or "").strip()
        samples.append(
            {
                "benchmark": "mvbench",
                "sample_id": f"mvbench_{i}",
                "video_path": str(video_path),
                "question": parsed_question,
                "options": options,
                "answer": answer,
                "task_type": meta.get("task_type", "Unknown"),
            }
        )
    return samples


def _resolve_mmvu_local_video(video: str, data_path: str = "") -> str:
    """Resolve local paths from the URL format used by MMVU validation.json."""
    if os.path.exists(video):
        return os.path.abspath(video)
    m = re.search(r"/videos/(.+/[^/]+\.mp4)$", video)
    if m:
        rel_path = m.group(1)
        candidates = []
        if data_path:
            data_dir = os.path.dirname(os.path.abspath(data_path))
            candidates.insert(0, os.path.join(data_dir, "videos", rel_path))
        video_root = os.environ.get("MMVU_VIDEO_ROOT")
        if video_root:
            candidates.insert(0, os.path.join(video_root, rel_path))
        for local in candidates:
            if os.path.exists(local):
                return os.path.abspath(local)
    return video


def _load_mmvu_samples(data_path: str) -> List[Dict[str, Any]]:
    rows = load_json_or_jsonl(data_path)
    samples: List[Dict[str, Any]] = []
    for row in rows:
        question_type = row.get("question_type", "multiple-choice")
        choices = row.get("choices", {})
        if question_type == "multiple-choice" and (not choices or all(not str(v).strip() for v in choices.values())):
            parsed_question, parsed_options = _parse_question_options_from_text(str(row["question"]))
            choice_dict = {}
            for opt in parsed_options:
                m = re.match(r"^([A-E])\.\s*(.*)$", opt)
                if m:
                    choice_dict[m.group(1)] = m.group(2)
            question = parsed_question
            choices = choice_dict
        else:
            question = str(row["question"]).strip()
        samples.append(
            {
                "benchmark": "mmvu",
                "sample_id": row.get("id", f"mmvu_{len(samples)}"),
                "video_path": _resolve_mmvu_local_video(str(row.get("video_path") or row.get("video")), data_path),
                "question": question,
                "choices": {str(k): str(v) for k, v in choices.items()},
                "question_type": question_type,
                "answer": str(row["answer"]),
                "subject": row.get("metadata", {}).get("subject", row.get("subject", "Unknown")),
            }
        )
    return samples


def _resolve_videomme_video_path(video_id: str, video_path: str, data_path: str) -> str:
    basename = f"{video_id}.mp4" if not video_path else os.path.basename(video_path)
    candidates = [
        video_path,
        os.path.join(os.path.dirname(data_path), basename),
    ]
    video_root = os.environ.get("VIDEOMME_VIDEO_ROOT")
    if video_root:
        candidates.insert(0, os.path.join(video_root, basename))
    return _resolve_existing_path(candidates)


def _load_videomme_tsv(data_path: str) -> List[Dict[str, Any]]:
    samples: List[Dict[str, Any]] = []
    counters: Dict[str, int] = {}
    with open(data_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        for row in reader:
            video_id = str(row["video"]).strip()
            counters[video_id] = counters.get(video_id, 0) + 1
            video_path = _resolve_videomme_video_path(video_id, str(row.get("video_path", "")).strip(), data_path)
            options = _safe_literal_list(row.get("candidates"))
            samples.append(
                {
                    "benchmark": "videomme",
                    "sample_id": f"{video_id}-{counters[video_id]}",
                    "video_id": video_id,
                    "video_path": video_path,
                    "duration": str(row["duration"]).strip(),
                    "domain": str(row["domain"]).strip(),
                    "sub_category": str(row["sub_category"]).strip(),
                    "task_type": str(row["task_type"]).strip(),
                    "question": str(row["question"]).strip(),
                    "options": options,
                    "answer": str(row["answer"]).strip(),
                }
            )
    return samples


def _load_videomme_jsonl_enriched(data_path: str) -> List[Dict[str, Any]]:
    rows = load_json_or_jsonl(data_path)
    tsv_path = os.path.join(os.path.dirname(data_path), "Video-MME.tsv")
    meta_index: Dict[Tuple[str, str, str], Dict[str, Any]] = {}
    if os.path.exists(tsv_path):
        for sample in _load_videomme_tsv(tsv_path):
            key = (os.path.basename(sample["video_path"]), sample["question"], sample["answer"])
            meta_index[key] = sample
    out: List[Dict[str, Any]] = []
    for i, row in enumerate(rows):
        q, parsed_options = _parse_question_options_from_text(str(row["question"]))
        key = (os.path.basename(str(row["video_path"])), q, str(row["answer"]).strip())
        meta = meta_index.get(key, {})
        out.append(
            {
                "benchmark": "videomme",
                "sample_id": meta.get("sample_id", f"videomme_{i}"),
                "video_id": meta.get("video_id", os.path.splitext(os.path.basename(str(row["video_path"])))[0]),
                "video_path": str(row["video_path"]),
                "duration": meta.get("duration", row.get("duration", "unknown")),
                "domain": meta.get("domain", row.get("domain", "Unknown")),
                "sub_category": meta.get("sub_category", row.get("sub_category", "Unknown")),
                "task_type": meta.get("task_type", row.get("task_type", "Unknown")),
                "question": q,
                "options": meta.get("options", parsed_options),
                "answer": str(row["answer"]).strip(),
            }
        )
    return out


