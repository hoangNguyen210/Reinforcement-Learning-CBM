def load_json_or_jsonl(path: str) -> List[Dict[str, Any]]:
    if path.endswith(".jsonl"):
        out = []
        with open(path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    out.append(json.loads(line))
        return out
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    if isinstance(data, list):
        return data
    raise ValueError(f"Unsupported data format in {path}: expected list/jsonl")


def _resolve_existing_path(candidates: List[str]) -> str:
    for path in candidates:
        if path and os.path.exists(path):
            return os.path.abspath(path)
    return os.path.abspath(candidates[0]) if candidates else ""


def _safe_literal_list(value: Any) -> List[str]:
    if isinstance(value, list):
        return [str(x) for x in value]
    if value is None:
        return []
    if isinstance(value, str):
        try:
            parsed = ast.literal_eval(value)
            if isinstance(parsed, list):
                return [str(x) for x in parsed]
        except Exception:
            pass
        return [x.strip() for x in value.split("|||") if x.strip()]
    return [str(x) for x in list(value)]


def _parse_question_options_from_text(question: str) -> Tuple[str, List[str]]:
    q_lines: List[str] = []
    options: List[str] = []
    for line in question.splitlines():
        m = re.match(r"^\s*-\s*([A-E])[\.\)]\s*(.*)$", line.strip())
        if m:
            options.append(f"{m.group(1).upper()}. {m.group(2).strip()}")
        else:
            q_lines.append(line.rstrip())
    return "\n".join([x for x in q_lines if x.strip()]).strip(), options


