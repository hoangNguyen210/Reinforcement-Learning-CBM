def save_video_frames_vlmevalkit(
    video_path: str,
    video_id: str,
    frame_root: str,
    nframe: int,
    lock_timeout: int = 300,
) -> List[str]:
    """
    Pre-decode video with decord and save frames to disk (VLMEvalKit style).
    step_size = len(vid) / (nframe + 1); indices = [int(i * step_size) for i in range(1, nframe + 1)]
    """
    import decord
    from PIL import Image

    frame_dir = os.path.join(frame_root, video_id)
    os.makedirs(frame_dir, exist_ok=True)
    frame_tmpl = "frame-{}-of-{}.jpg"
    frame_paths = [os.path.join(frame_dir, frame_tmpl.format(i, nframe)) for i in range(1, nframe + 1)]

    if all(os.path.exists(p) for p in frame_paths):
        return frame_paths

    try:
        import portalocker
    except ImportError:
        portalocker = None

    lock_path = os.path.join(frame_root, video_id + ".lock") if portalocker else None

    def _do_save():
        if all(os.path.exists(p) for p in frame_paths):
            return frame_paths
        vid = decord.VideoReader(video_path)
        n_frames = len(vid)
        if n_frames <= 0:
            raise ValueError(f"Video has no frames: {video_path}")
        step_size = n_frames / (nframe + 1)
        indices = []
        for i in range(1, nframe + 1):
            idx = int(i * step_size)
            idx = min(idx, n_frames - 1)
            indices.append(idx)
        images = [vid[i].asnumpy() for i in indices]
        images = [Image.fromarray(arr) for arr in images]
        for im, pth in zip(images, frame_paths):
            if not os.path.exists(pth):
                im.save(pth)
        return frame_paths

    if portalocker and lock_path:
        with portalocker.Lock(lock_path, "w", timeout=lock_timeout):
            return _do_save()
    return _do_save()


