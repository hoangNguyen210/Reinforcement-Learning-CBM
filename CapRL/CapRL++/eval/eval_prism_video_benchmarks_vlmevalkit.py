def _timelens_subset_and_subdir(data_path: str) -> Tuple[str, str]:
    """
    Infer TimeLens subset folder from annotation JSON filename.
    Expects e.g. charades-timelens.json / activitynet-timelens.json / qvhighlights-timelens.json.
    """
    base = os.path.basename(data_path).lower()
    if "charades" in base:
        return "charades", "charades"
    if "activitynet" in base:
        return "activitynet", "activitynet"
    if "qvhighlights" in base:
        return "qvhighlights", "qvhighlights"
    raise ValueError(
        f"Cannot infer TimeLens subset from {data_path!r}; "
        "use a filename containing charades, activitynet, or qvhighlights "
        "(e.g. charades-timelens.json)."
    )


def _load_timelens_json(data_path: str) -> List[Dict[str, Any]]:
    """
    Load TimeLens-Bench JSON: { video_id: { duration, spans, queries } }.
    Videos: <TIMELENS_VIDEO_ROOT or dirname(data_path)/videos>/<subdir>/<video_id>.mp4
    """
    subset, subdir = _timelens_subset_and_subdir(data_path)
    bench_root = os.path.dirname(os.path.abspath(data_path))
    video_root = os.environ.get("TIMELENS_VIDEO_ROOT", os.path.join(bench_root, "videos"))
    video_sub = os.path.join(video_root, subdir)

    with open(data_path, "r", encoding="utf-8") as f:
        annotations = json.load(f)
    if not isinstance(annotations, dict):
        raise ValueError(f"TimeLens JSON must be a dict keyed by video id, got {type(annotations)}")

    samples: List[Dict[str, Any]] = []
    for vid_key, obj in annotations.items():
        if not isinstance(obj, dict):
            continue
        spans = obj.get("spans") or []
        queries = obj.get("queries") or []
        n = min(len(spans), len(queries))
        try:
            duration = float(obj.get("duration", 0.0))
        except (TypeError, ValueError):
            duration = 0.0
        video_path = os.path.join(video_sub, f"{vid_key}.mp4")
        if not os.path.isfile(video_path):
            continue
        for j in range(n):
            span = spans[j]
            query = str(queries[j]).strip()
            if not query:
                continue
            if not isinstance(span, (list, tuple)) or len(span) < 2:
                continue
            try:
                gt_start = float(span[0])
                gt_end = float(span[1])
            except (TypeError, ValueError):
                continue
            if gt_start > gt_end:
                gt_start, gt_end = gt_end, gt_start
            samples.append(
                {
                    "benchmark": "timelens",
                    "timelens_subset": subset,
                    "sample_id": f"timelens_{subset}_{vid_key}_{j}",
                    "video_id": str(vid_key),
                    "video_path": video_path,
                    "question": query,
                    "caption_query": query,
                    "gt_start": gt_start,
                    "gt_end": gt_end,
                    "duration_meta": duration,
                }
            )
    return samples


def _load_timelens_from_data_path(data_path: str) -> List[Dict[str, Any]]:
    """
    Load one JSON file, or every *timelens.json under a directory (full TimeLens-Bench in one job).
    """
    ap = os.path.abspath(os.path.expanduser(data_path))
    if os.path.isfile(ap):
        if not ap.lower().endswith(".json"):
            raise ValueError("timelens: when --data-path is a file, it must be a .json annotation")
        return _load_timelens_json(ap)
    if os.path.isdir(ap):
        files = sorted(glob.glob(os.path.join(ap, "*timelens.json")))
        if not files:
            raise ValueError(
                f"timelens: no *timelens.json found under {ap}. "
                "Use the TimeLens-Bench root (with charades/activitynet/qvhighlights-timelens.json) "
                "or pass a single .json path."
            )
        merged: List[Dict[str, Any]] = []
        for fp in files:
            merged.extend(_load_timelens_json(fp))
        return merged
    raise ValueError(
        f"timelens: --data-path not found (expect a *-timelens.json file or dataset directory): {data_path}"
    )


def temporal_iou(
    pred_start: float, pred_end: float, gt_start: float, gt_end: float
) -> float:
    """Temporal IoU on real line (seconds). Empty or invalid intervals -> 0."""
    if pred_start > pred_end:
        pred_start, pred_end = pred_end, pred_start
    if gt_start > gt_end:
        gt_start, gt_end = gt_end, gt_start
    inter = max(0.0, min(pred_end, gt_end) - max(pred_start, gt_start))
    len_p = pred_end - pred_start
    len_g = gt_end - gt_start
    union = len_p + len_g - inter
    if union <= 0.0:
        return 0.0
    return float(inter / union)


def _strip_markdown_code_fences(text: str) -> str:
    """Remove ```json ... ``` wrappers; models often wrap JSON in fences."""
    s = text.strip()
    m = re.match(r"^```[a-zA-Z0-9_-]*\s*\r?\n?(.*)\r?\n?```\s*$", s, flags=re.DOTALL)
    if m:
        return m.group(1).strip()
    if s.startswith("```"):
        s = re.sub(r"^```[a-zA-Z0-9_-]*\s*", "", s, count=1)
        s = re.sub(r"\s*```\s*$", "", s, count=1)
    return s.strip()


def _parse_scalar_to_seconds(val: Any) -> Optional[float]:
    """
    Convert JSON value to seconds: float/int, or str as plain float or M:SS / H:MM:SS.
    """
    if val is None:
        return None
    if isinstance(val, bool):
        return None
    if isinstance(val, (int, float)):
        return float(val)
    if not isinstance(val, str):
        return None
    s = val.strip()
    s = s.strip('"').strip("'")
    s = s.replace('\\"', '"').replace("\\'", "'").strip('"').strip("'")
    if not s:
        return None
    s_norm = s.replace("：", ":").replace("，", ",")
    if ":" in s_norm:
        parts = [p.strip() for p in s_norm.split(":")]
        try:
            if len(parts) == 2:
                return float(parts[0]) * 60.0 + float(parts[1])
            if len(parts) == 3:
                return float(parts[0]) * 3600.0 + float(parts[1]) * 60.0 + float(parts[2])
        except ValueError:
            return None
        return None
    try:
        return float(s_norm)
    except ValueError:
        return None


def _normalize_jsonish_text(text: str) -> str:
    """Light cleanup before json.loads (stray backslash before quote in keys)."""
    s = _strip_markdown_code_fences(text)
    if s.count('\\"') >= 2 and s.lstrip().startswith("{"):
        s2 = s.replace('\\"', '"')
        if s2 != s:
            s = s2
    return s


def _iter_balanced_brace_objects(s: str):
    depth = 0
    start_idx = -1
    for i, c in enumerate(s):
        if c == "{":
            if depth == 0:
                start_idx = i
            depth += 1
        elif c == "}":
            if depth > 0:
                depth -= 1
                if depth == 0 and start_idx >= 0:
                    yield s[start_idx : i + 1]
                    start_idx = -1


def _interval_from_mapping(obj: Dict[str, Any]) -> Optional[Tuple[float, float]]:
    if not isinstance(obj, dict):
        return None
    norm: Dict[str, Any] = {}
    for k, v in obj.items():
        kn = str(k).strip().lower().strip('"').strip("'").replace("\\", "")
        norm[kn] = v
    for k1, k2 in (
        ("start", "end"),
        ("t_start", "t_end"),
        ("begin", "end"),
        ("start_time", "end_time"),
    ):
        if k1 in norm and k2 in norm:
            a = _parse_scalar_to_seconds(norm[k1])
            b = _parse_scalar_to_seconds(norm[k2])
            if a is not None and b is not None:
                return (a, b)
    return None


def _interval_from_sequence(obj: Any) -> Optional[Tuple[float, float]]:
    if isinstance(obj, (list, tuple)) and len(obj) >= 2:
        a = _parse_scalar_to_seconds(obj[0])
        b = _parse_scalar_to_seconds(obj[1])
        if a is not None and b is not None:
            return (a, b)
    return None


def _regex_extract_start_end_interval(text: str) -> Optional[Tuple[float, float]]:
    """
    Fallback when JSON is broken: "start": ..., "end": ... or start\": \"0:25\".
    """
    p_quoted = re.compile(
        r'(?is)["\']start["\']\s*:\s*'
        r'(?:["\']([^"\']+)["\']|([-+]?\d*\.?\d+))\s*[,，]\s*'
        r'["\']end["\']\s*:\s*'
        r'(?:["\']([^"\']+)["\']|([-+]?\d*\.?\d+))'
    )
    m = p_quoted.search(text)
    if m:
        s_raw = m.group(1) or m.group(2)
        e_raw = m.group(3) or m.group(4)
        if s_raw is not None and e_raw is not None:
            a = _parse_scalar_to_seconds(s_raw.strip())
            b = _parse_scalar_to_seconds(e_raw.strip())
            if a is not None and b is not None:
                return (a, b)

    p_slash = re.compile(
        r'(?is)start\s*\\?["\']?\s*:\s*\\?["\']?'
        r'([0-9]+(?::[0-9]+(?:\.[0-9]+)?){0,2}|[-+]?\d*\.?\d+)\s*\\?["\']?\s*[,，]\s*'
        r'end\s*\\?["\']?\s*:\s*\\?["\']?'
        r'([0-9]+(?::[0-9]+(?:\.[0-9]+)?){0,2}|[-+]?\d*\.?\d+)\s*\\?["\']?'
    )
    m = p_slash.search(text)
    if m:
        a = _parse_scalar_to_seconds(m.group(1).strip())
        b = _parse_scalar_to_seconds(m.group(2).strip())
        if a is not None and b is not None:
            return (a, b)
    return None


def parse_time_interval_from_text(text: str) -> Optional[Tuple[float, float]]:
    """
    Best-effort parse of (start, end) in seconds from model output.
    Handles: markdown fences, multi-line JSON, start/end as strings "0:25", M:SS / H:MM:SS,
    and simple numeric pairs. Returns None if not parseable.
    """
    if not text or not isinstance(text, str):
        return None
    s = _normalize_jsonish_text(text)

    # Whole string JSON
    for candidate in (s, text.strip()):
        if candidate.startswith("{") or candidate.startswith("["):
            try:
                obj = json.loads(candidate)
            except json.JSONDecodeError:
                obj = None
            if obj is not None:
                got = _interval_from_sequence(obj)
                if got:
                    return got
                if isinstance(obj, dict):
                    got = _interval_from_mapping(obj)
                    if got:
                        return got

    # Balanced { ... } substrings (multi-line objects)
    for frag in _iter_balanced_brace_objects(s):
        try:
            obj = json.loads(frag)
        except json.JSONDecodeError:
            continue
        got = _interval_from_sequence(obj)
        if got:
            return got
        if isinstance(obj, dict):
            got = _interval_from_mapping(obj)
            if got:
                return got

    # Legacy: compact single-line {...} / [...]
    for m in re.finditer(r"\{[^{}]+\}|\[[^\[\]]+\]", s):
        frag = m.group(0)
        try:
            obj = json.loads(frag)
        except json.JSONDecodeError:
            continue
        got = _interval_from_sequence(obj)
        if got:
            return got
        if isinstance(obj, dict):
            got = _interval_from_mapping(obj)
            if got:
                return got

    reg = _regex_extract_start_end_interval(s)
    if reg:
        return reg

    # M:SS or H:MM:SS — M:SS (two times separated by dash/comma)
    m = re.search(
        r"([0-9]+(?::[0-9]+(?:\.[0-9]+)?){1,2})\s*[-–~,，]\s*([0-9]+(?::[0-9]+(?:\.[0-9]+)?){1,2})",
        s,
    )
    if m:
        a = _parse_scalar_to_seconds(m.group(1))
        b = _parse_scalar_to_seconds(m.group(2))
        if a is not None and b is not None:
            return (a, b)

    second_marker = "\u79d2"
    m = re.search(
        f"(?:\\[|\\b)\\s*([-+]?\\d*\\.?\\d+)\\s*{second_marker}\\s*[:\\uff1a\\-\\u2013~,\\uff0c]\\s*"
        f"([-+]?\\d*\\.?\\d+)\\s*{second_marker}\\s*(?:\\]|\\b)",
        s,
    )
    if not m:
        m = re.search(
            f"\\b([-+]?\\d*\\.?\\d+)\\s*{second_marker}\\s*[:\\uff1a\\-\\u2013~,\\uff0c]\\s*"
            f"([-+]?\\d*\\.?\\d+)\\s*{second_marker}\\b",
            s,
        )
    if m:
        try:
            return (float(m.group(1)), float(m.group(2)))
        except ValueError:
            pass

    # Plain float pair: "12.3 - 45.6" or "12.3, 45.6"
    m = re.search(
        r"(-?\d+(?:\.\d+)?)\s*[-–~,，]\s*(-?\d+(?:\.\d+)?)",
        s,
    )
    if m:
        try:
            return (float(m.group(1)), float(m.group(2)))
        except ValueError:
            pass
    return None


def _scale_unit_fraction_interval(
    ps: float, pe: float, gt_start: float, gt_end: float
) -> Tuple[float, float]:
    """
    Scale unit-fraction predictions to seconds for legacy outputs.
    """
    if ps > pe:
        ps, pe = pe, ps
    if 0.0 <= ps < pe <= 1.0 + 1e-9 and max(gt_start, gt_end) > 1.0:
        return ps * 100.0, pe * 100.0
    return ps, pe


def temporal_response_iou(pred: str, sample: Dict[str, Any]) -> float:
    """IoU between parsed prediction and sample gt_start/gt_end (seconds)."""
    parsed = parse_time_interval_from_text(pred)
    if parsed is None:
        return 0.0
    ps, pe = parsed
    gt0, gt1 = float(sample["gt_start"]), float(sample["gt_end"])
    ps, pe = _scale_unit_fraction_interval(ps, pe, gt0, gt1)
    return temporal_iou(ps, pe, gt0, gt1)


