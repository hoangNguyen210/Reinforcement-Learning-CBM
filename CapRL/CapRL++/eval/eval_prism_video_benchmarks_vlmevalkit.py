def extract_option_from_text(text: str, allow_a2j: bool = False) -> str:
    """Extract option letter (A-E by default; A-J if allow_a2j, for TOMATO etc)."""
    if not isinstance(text, str):
        return ""
    pat = r"\b([A-J])\b" if allow_a2j else r"\b([A-E])\b"
    m = re.search(pat, text.strip().upper())
    return m.group(1) if m else ""


def _mvbench_answer_to_letter(sample: Dict[str, Any]) -> str:
    """Convert an MVBench answer string to an option letter."""
    answer_raw = str(sample.get("answer", "")).strip()
    options = sample.get("options", [])
    if not options:
        return answer_raw.upper()
    if re.fullmatch(r"^[A-E]$", answer_raw, flags=re.IGNORECASE):
        return answer_raw.upper()
    for opt in options:
        m = re.match(r"^([A-E])\.\s*(.*)$", opt.strip())
        if m and m.group(2).strip() == answer_raw:
            return m.group(1).upper()
    return answer_raw.upper()


def eval_mvbench_check_ans_like_official(pred: str, gt: str) -> float:
    """Evaluate MVBench after converting the answer to an option letter."""
    pred_list = pred.lower().strip().split(" ")
    if not pred_list:
        return 0.0
    pred_option = pred_list[0]
    gt = gt.strip()
    if re.fullmatch(r"[A-E]", gt, flags=re.IGNORECASE):
        gt_option = f"({gt.upper()})".lower()
    else:
        gt_option = gt.lower().split(" ")[0]
    if pred_option.replace(".", "") in gt_option:
        return 1.0
    if gt_option in pred_option:
        return 1.0
    return 1.0 if extract_option_from_text(pred) == gt_option.replace("(", "").replace(")", "").upper() else 0.0


def extract_characters_regex_videomme(s: str) -> str:
    s = (s or "").strip()
    answer_prefixes = [
        "The best answer is",
        "The correct answer is",
        "The answer is",
        "The answer",
        "The best option isThe correct option is",
        "Best answer:Best option:",
        "Answer:",
        "Option:",
        "The correct answer",
        "The correct option",
    ]
    for answer_prefix in answer_prefixes:
        s = s.replace(answer_prefix, "")
    if len(s.split()) > 10 and not re.search("[ABCD]", s):
        return ""
    matches = re.search(r"[ABCD]", s)
    if matches is None:
        return ""
    return matches[0]


def eval_videomme_response(pred: str, gt: str) -> float:
    ext = extract_characters_regex_videomme(pred)
    return 1.0 if ext == gt.strip().upper() else 0.0


def eval_tomato_response(pred: str, gt: str) -> float:
    """TOMATO: answer is option letter A-F, extract and compare."""
    ext = extract_option_from_text(pred, allow_a2j=True)
    return 1.0 if ext == gt.strip().upper() else 0.0


def build_mmvu_judge_messages(sample: Dict[str, Any], response: str) -> List[Dict[str, str]]:
    question = str(sample["question"])
    gt = str(sample["answer"])
    question_type = sample.get("question_type", "multiple-choice")
    choices = sample.get("choices", {})
    system_instruction = MMVU_MC_INSTRUCTION if question_type == "multiple-choice" else MMVU_OE_INSTRUCTION
    if question_type == "multiple-choice" and choices:
        optionized = "\n".join([f"{k}: {v}" for k, v in choices.items()])
        user_prompt = (
            f"Question: {question}\n\n"
            f"Options:\n{optionized}\n\n"
            f"Ground Truth Answer: {gt}\n\n"
            f"Model Response to the Question: {response}"
        )
    else:
        user_prompt = (
            f"Question: {question}\n\n"
            f"Ground Truth Answer: {gt}\n\n"
            f"Model Response to the Question: {response}"
        )
    return [{"role": "system", "content": system_instruction}, {"role": "user", "content": user_prompt}]


async def mmvu_judge_one(client: Any, model: str, sample: Dict[str, Any], response: str) -> Tuple[str, float]:
    messages = build_mmvu_judge_messages(sample, response)
    out = await client.chat.completions.create(
        model=model,
        messages=messages,
        temperature=0.0,
        max_tokens=64,
        response_format={"type": "json_object"},
    )
    content = out.choices[0].message.content or "{}"
    try:
        parsed = json.loads(content)
        extracted = str(parsed.get("extracted_answer", "")).strip().upper()
        correct = bool(parsed.get("correct", False))
        return extracted, 1.0 if correct else 0.0
    except Exception:
        return "", 0.0


async def eval_mmvu_with_llm_judge(
    items: List[Tuple[Dict[str, Any], str]],
    judge_model: str,
    max_concurrency: int = 16,
) -> List[Tuple[str, float]]:
    from openai import AsyncOpenAI

    api_key = os.getenv("OPENAI_API_KEY")
    base_url = os.getenv("OPENAI_BASE_URL")
    if not api_key:
        raise RuntimeError(
            "MMVU judge requires API key. Set OPENAI_API_KEY."
        )

    client_kwargs: Dict[str, Any] = {"api_key": api_key}
    if base_url:
        client_kwargs["base_url"] = base_url
    client = AsyncOpenAI(**client_kwargs)
    sem = asyncio.Semaphore(max_concurrency)

    async def _run(sample: Dict[str, Any], response: str):
        async with sem:
            return await mmvu_judge_one(client, judge_model, sample, response)

    tasks = [_run(sample, resp) for sample, resp in items]
    return await asyncio.gather(*tasks)


def evaluate_responses_for_benchmark(
    benchmark: str,
    samples: List[Dict[str, Any]],
    all_responses: List[List[str]],
    mmvu_judge_model: str,
    mmvu_judge_concurrency: int,
) -> Tuple[List[List[float]], Optional[List[List[str]]]]:
    extracted_answers: Optional[List[List[str]]] = None

    if benchmark in ("mvbench", "videomme", "tomato"):
        per_sample_scores: List[List[float]] = []
        per_sample_extract: List[List[str]] = []
        for sample, responses in zip(samples, all_responses):
            if benchmark == "mvbench":
                gt = _mvbench_answer_to_letter(sample)
            else:
                gt = str(sample["answer"]).strip().upper()
            cur_scores: List[float] = []
            cur_extract: List[str] = []
            for resp in responses:
                if benchmark == "mvbench":
                    score = eval_mvbench_check_ans_like_official(resp, gt)
                    ext = extract_option_from_text(resp)
                elif benchmark == "videomme":
                    score = eval_videomme_response(resp, gt)
                    ext = extract_characters_regex_videomme(resp)
                else:
                    score = eval_tomato_response(resp, gt)
                    ext = extract_option_from_text(resp, allow_a2j=True)
                cur_scores.append(score)
                cur_extract.append(ext)
            per_sample_scores.append(cur_scores)
            per_sample_extract.append(cur_extract)
        extracted_answers = per_sample_extract
        return per_sample_scores, extracted_answers

    if benchmark == "timelens":
        per_sample_scores: List[List[float]] = []
        per_sample_extract: List[List[str]] = []
        for sample, responses in zip(samples, all_responses):
            cur_scores: List[float] = []
            cur_extract: List[str] = []
            for resp in responses:
                iou = temporal_response_iou(resp, sample)
                cur_scores.append(iou)
                parsed = parse_time_interval_from_text(resp)
                if parsed:
                    ps, pe = parsed
                    gt0, gt1 = float(sample["gt_start"]), float(sample["gt_end"])
                    ps, pe = _scale_unit_fraction_interval(ps, pe, gt0, gt1)
                    cur_extract.append(
                        json.dumps({"start": ps, "end": pe}, ensure_ascii=False)
                    )
                else:
                    cur_extract.append("")
            per_sample_scores.append(cur_scores)
            per_sample_extract.append(cur_extract)
        extracted_answers = per_sample_extract
        return per_sample_scores, extracted_answers

    if benchmark == "mmvu":
        flattened: List[Tuple[Dict[str, Any], str]] = []
        sizes: List[int] = []
        for sample, responses in zip(samples, all_responses):
            sizes.append(len(responses))
            for resp in responses:
                flattened.append((sample, resp))

        judged = asyncio.run(
            eval_mmvu_with_llm_judge(
                flattened,
                judge_model=mmvu_judge_model,
                max_concurrency=mmvu_judge_concurrency,
            )
        )

        per_sample_scores = []
        per_sample_extract = []
        idx = 0
        for sz in sizes:
            sub = judged[idx : idx + sz]
            idx += sz
            per_sample_extract.append([x[0] for x in sub])
            per_sample_scores.append([x[1] for x in sub])
        extracted_answers = per_sample_extract
        return per_sample_scores, extracted_answers

    raise ValueError(f"Unsupported benchmark: {benchmark}")


def compute_extra_metrics(benchmark: str, sample_results: List[SampleResult]) -> Dict[str, Any]:
    def _agg(key_name: str) -> Dict[str, Dict[str, float]]:
        grouped_bon: Dict[str, List[float]] = {}
        grouped_macc: Dict[str, List[float]] = {}
        for r in sample_results:
            key = str(r.sample.get(key_name, "Unknown"))
            grouped_bon.setdefault(key, []).append(r.bon)
            grouped_macc.setdefault(key, []).append(r.m_acc)
        return {
            "bon": {k: float(np.mean(v)) if v else 0.0 for k, v in sorted(grouped_bon.items())},
            "m_acc": {k: float(np.mean(v)) if v else 0.0 for k, v in sorted(grouped_macc.items())},
        }

    if benchmark == "videomme":
        return {
            "duration": _agg("duration"),
            "domain": _agg("domain"),
            "sub_category": _agg("sub_category"),
            "task_type": _agg("task_type"),
        }
    if benchmark == "mvbench":
        return {"task_type": _agg("task_type")}
    if benchmark == "mmvu":
        return {"question_type": _agg("question_type"), "subject": _agg("subject")}
    if benchmark == "tomato":
        return {"task_type": _agg("task_type"), "demonstration_type": _agg("demonstration_type")}
    if benchmark == "timelens":
        return {"timelens_subset": _agg("timelens_subset")}
    return {}


def build_videomme_official_result_files(
    sample_results: List[SampleResult], save_dir: str, step: int
) -> Dict[str, str]:
    grouped_first: Dict[str, Dict[str, Any]] = {}
    grouped_bon: Dict[str, Dict[str, Any]] = {}
    for r in sample_results:
        sample = r.sample
        video_id = str(sample["video_id"])
        if video_id not in grouped_first:
            base = {
                "video_id": video_id,
                "duration": sample["duration"],
                "domain": sample["domain"],
                "sub_category": sample["sub_category"],
                "questions": [],
            }
            grouped_first[video_id] = json.loads(json.dumps(base))
            grouped_bon[video_id] = json.loads(json.dumps(base))
        best_idx = int(np.argmax(r.correctness)) if r.correctness else 0
        common = {
            "question_id": sample["sample_id"],
            "task_type": sample["task_type"],
            "question": sample["question"],
            "options": sample.get("options", []),
            "answer": sample["answer"],
        }
        grouped_first[video_id]["questions"].append(
            {
                **common,
                "response": r.responses[0] if r.responses else "",
                "responses": r.responses,
                "correctness": r.correctness,
                "bon": r.bon,
                "m_acc": r.m_acc,
            }
        )
        grouped_bon[video_id]["questions"].append(
            {
                **common,
                "response": r.responses[best_idx] if r.responses else "",
                "responses": r.responses,
                "correctness": r.correctness,
                "bon": r.bon,
                "m_acc": r.m_acc,
            }
        )

    first_path = os.path.join(save_dir, f"videomme_official_first_step{step}.json")
    bon_path = os.path.join(save_dir, f"videomme_official_bon_step{step}.json")
    save_json(first_path, list(grouped_first.values()))
    save_json(bon_path, list(grouped_bon.values()))
    return {"videomme_official_first": first_path, "videomme_official_bon": bon_path}


