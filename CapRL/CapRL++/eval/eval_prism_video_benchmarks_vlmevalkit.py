def _stage1_caption_api(
    samples: List[Dict[str, Any]], args: argparse.Namespace, log_prefix: str = "[Stage1-API]"
) -> List[Dict[str, Any]]:
    """Stage1 via vLLM OpenAI API: send video_url, server decodes."""
    import time
    from openai import AsyncOpenAI

    api_base = args.vllm_api_base.rstrip("/")
    model_name = getattr(args, "caption_model_name", None) or os.path.basename(args.caption_model_path.rstrip("/"))
    concurrency = int(getattr(args, "api_concurrency", 8))
    client = AsyncOpenAI(api_key="EMPTY", base_url=api_base)
    sem = asyncio.Semaphore(concurrency)

    extra_body: Optional[Dict[str, Any]] = None
    raw_extra = getattr(args, "caption_extra_body", None)
    if raw_extra:
        extra_body = json.loads(raw_extra)
    enable_thinking = getattr(args, "enable_thinking", None)
    if enable_thinking is not None:
        thinking_val = enable_thinking.lower() in ("true", "1", "yes")
        if extra_body is None:
            extra_body = {}
        extra_body.setdefault("chat_template_kwargs", {})["enable_thinking"] = thinking_val

    async def _caption_one(sample: Dict[str, Any]) -> Dict[str, Any]:
        video_path = ensure_video_path(sample)
        messages = _build_caption_messages_video_url(video_path, benchmark=args.benchmark)
        create_kwargs: Dict[str, Any] = dict(
            model=model_name,
            messages=messages,
            n=args.gen_num,
            temperature=args.caption_temperature,
            max_tokens=args.caption_max_tokens,
        )
        if extra_body:
            create_kwargs["extra_body"] = extra_body
        async with sem:
            resp = await client.chat.completions.create(**create_kwargs)
        texts = [c.message.content or "" for c in resp.choices]
        return {"sample": sample, "captions": texts[: args.gen_num]}

    async def _run_batch(batch: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        tasks = [_caption_one(s) for s in batch]
        return await asyncio.gather(*tasks, return_exceptions=True)

    caption_records: List[Dict[str, Any]] = []
    total_batches = (len(samples) + args.caption_batch_size - 1) // args.caption_batch_size if samples else 0
    start_time = time.time()
    for batch_idx, i in enumerate(range(0, len(samples), args.caption_batch_size), start=1):
        batch = samples[i : i + args.caption_batch_size]
        results = asyncio.run(_run_batch(batch))
        for j, r in enumerate(results):
            if isinstance(r, Exception):
                video_path = ensure_video_path(batch[j])
                print(f"[WARN] skip video {video_path}: {r}")
                caption_records.append(
                    {"sample": batch[j], "captions": ["[video_api_failed]"] * args.gen_num}
                )
            else:
                caption_records.append(r)
        if batch_idx == 1 or batch_idx % 5 == 0 or batch_idx == total_batches:
            _log_batch_progress(log_prefix, batch_idx, total_batches, start_time)
    return caption_records


