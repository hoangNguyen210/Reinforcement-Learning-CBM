#!/usr/bin/env python3
"""Standalone MMVU judge for saved Prism inference outputs."""

import argparse
import json
import os
import re
import sys
from concurrent.futures import ThreadPoolExecutor, as_completed
from dataclasses import dataclass
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import httpx
from openai import OpenAI
from tqdm import tqdm

_http_client = httpx.Client(proxy=None, timeout=60.0)
gpt_client = OpenAI(
    base_url=os.getenv("OPENAI_BASE_URL"),
    api_key=os.getenv("OPENAI_API_KEY", ""),
    http_client=_http_client,
)

MMVU_MC_INSTRUCTION = """Evaluate whether the model's final answer is correct by comparing it to the ground-truth answer provided for the given question.

You should first extract the final answer from the model's response, and then compare the extracted answer with the ground-truth answer to determine its accuracy.

You must respond with a valid json object only. Output your response in the following json format:
{
    "extracted_answer": "A" or "B" or "C" or "D" or "E",
    "correct": true or false
}
"""

MMVU_OE_INSTRUCTION = """Evaluate whether the model's final answer is correct by comparing it to the ground-truth answer provided for the given question.

You should first extract the final answer from the model's response, and then compare the extracted answer with the ground-truth answer to determine its accuracy.

The final answer generated by the model does not need to match the ground-truth answer word-for-word. However, it should only be considered correct if it demonstrates the exact same technique or concept explicitly and unambiguously equivalent to the ground-truth answer.

You must respond with a valid json object only. Output your response in the following json format:
{
    "extracted_answer": "the short final answer extracted from the model's response",
    "correct": true or false
}
"""


def _parse_judge_json_impl(content: str) -> Tuple[str, bool]:
    content = (content or "").strip()
    for prefix in ("```json", "```"):
        if content.startswith(prefix):
            content = content[len(prefix) :].replace("```", "").strip()
    if not content.startswith("{"):
        i = content.find("{")
        if i >= 0:
            content = content[i:]
    if not content.endswith("}"):
        i = content.rfind("}")
        if i >= 0:
            content = content[: i + 1]
    try:
        p = json.loads(content)
        return str(p.get("extracted_answer", "")).strip(), bool(p.get("correct", False))
    except Exception:
        return "", False


_MAX_OPTION_CHARS = 80


def _process_one_judge(inputs: Tuple[int, Dict, str, str]) -> Tuple[int, Tuple[str, float]]:
    """Judge one sample with the shared OpenAI client."""
    idx, sample, resp, judge_model = inputs
    question = str(sample["question"])
    gt = str(sample["answer"])
    qtype = sample.get("question_type", "multiple-choice")
    choices = sample.get("choices", {})
    instr = MMVU_MC_INSTRUCTION if qtype == "multiple-choice" else MMVU_OE_INSTRUCTION
    if qtype == "multiple-choice" and choices:
        parts = []
        for k, v in choices.items():
            v_str = str(v).strip()
            if len(v_str) > _MAX_OPTION_CHARS:
                v_str = v_str[:_MAX_OPTION_CHARS].rstrip() + "..."
            parts.append(f"{k}: {v_str}" if v_str else f"{k}:")
        opt = "\n".join(parts)
        user = f"Question: {question}\n\nOptions:\n{opt}\n\nGround Truth Answer: {gt}\n\nModel Response to the Question: {resp}"
    else:
        user = f"Question: {question}\n\nGround Truth Answer: {gt}\n\nModel Response to the Question: {resp}"
    messages = [{"role": "system", "content": instr}, {"role": "user", "content": user}]

    try:
        out = gpt_client.chat.completions.create(model=judge_model, messages=messages, temperature=0.0, max_tokens=128)
        content = out.choices[0].message.content or "{}"
        extracted, correct = _parse_judge_json_impl(content)
        if qtype == "multiple-choice":
            extracted = extracted.upper()
        return idx, (extracted, 1.0 if correct else 0.0)
    except Exception:
        return idx, ("", 0.0)


@dataclass
class SampleResult:
    sample: Dict[str, Any]
    captions: List[str]
    responses: List[str]
    correctness: List[float]

    @property
    def bon(self) -> float:
        return float(np.max(self.correctness)) if self.correctness else 0.0

    @property
    def m_acc(self) -> float:
        return float(np.mean(self.correctness)) if self.correctness else 0.0


def extract_option_from_text(text: str) -> str:
    """Extract an A-E option from model output."""
    if not isinstance(text, str):
        return ""
    s = (text or "").strip()
    answer_prefixes = [
        "The best answer is", "The correct answer is", "The answer is", "The answer",
        "The best option is", "The correct option is", "Best answer:", "Best option:",
        "Answer:", "Option:", "The correct answer", "The correct option",
    ]
    for prefix in answer_prefixes:
        s = s.replace(prefix, "")
    m = re.search(r"\b([A-E])\b", s.strip().upper())
    return m.group(1) if m else ""


def eval_mmvu_mc_rule_based(pred: str, gt: str) -> Tuple[str, float]:
    """Rule-based multiple-choice scoring."""
    pred_opt = extract_option_from_text(pred)
    gt_opt = re.sub(r"[^A-E]", "", str(gt).strip().upper())
    if not gt_opt:
        gt_opt = str(gt).strip().upper()[:1]
    score = 1.0 if pred_opt == gt_opt else 0.0
    return pred_opt, score


def eval_mmvu_with_llm_judge(
    items: List[Tuple[Dict[str, Any], str]],
    judge_model: str,
    max_concurrency: int = 16,
    max_retries: int = 3,
    rpm_limit: int = 0,
) -> List[Tuple[str, float]]:
    """Run judge requests with a shared HTTP connection pool."""
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        raise RuntimeError("MMVU judge requires API key. Set OPENAI_API_KEY.")

    inputs = [(i, s, r, judge_model) for i, (s, r) in enumerate(items)]
    results: List[Tuple[str, float]] = [("", 0.0)] * len(items)

    with ThreadPoolExecutor(max_workers=max_concurrency) as executor:
        futures = {executor.submit(_process_one_judge, inp): inp for inp in inputs}
        for future in tqdm(as_completed(futures), total=len(inputs), desc="GPT Judge"):
            idx, out = future.result()
            results[idx] = out

    return results


def evaluate_mmvu(
    records: List[Dict[str, Any]],
    judge_mode: int,
    judge_model: str,
    judge_concurrency: int,
    rpm_limit: int = 50,
) -> Tuple[List[SampleResult], List[List[str]]]:
    """
    judge_mode 1: rule-based multiple choice, GPT for open-ended questions.
    judge_mode 2: GPT for all questions.
    """
    samples = [r["sample"] for r in records]
    all_responses = [r["responses"] for r in records]
    per_sample_extract: List[List[str]] = []
    per_sample_scores: List[List[float]] = []

    if judge_mode == 1:
        mc_indices: List[Tuple[int, int]] = []  # (sample_idx, resp_idx)
        oe_items: List[Tuple[Dict[str, Any], str]] = []
        for i, (sample, responses) in enumerate(zip(samples, all_responses)):
            qtype = sample.get("question_type", "multiple-choice")
            for j, resp in enumerate(responses):
                if qtype == "multiple-choice":
                    mc_indices.append((i, j))
                else:
                    oe_items.append((sample, resp))

        print(f"  [Judge] {len(mc_indices)} multiple-choice items -> rule-based")
        mc_scores: Dict[Tuple[int, int], Tuple[str, float]] = {}
        for i, j in mc_indices:
            sample, resp = samples[i], all_responses[i][j]
            ext, score = eval_mmvu_mc_rule_based(resp, sample["answer"])
            mc_scores[(i, j)] = (ext, score)

        oe_results: List[Tuple[str, float]] = []
        if oe_items:
            print(f"  [Judge] {len(oe_items)} open-ended items -> GPT ({judge_concurrency} threads)")
            oe_results = eval_mmvu_with_llm_judge(
                oe_items,
                judge_model=judge_model,
                max_concurrency=judge_concurrency,
                rpm_limit=rpm_limit,
            )

        oe_idx = 0
        for i, (sample, responses) in enumerate(zip(samples, all_responses)):
            cur_scores: List[float] = []
            cur_extract: List[str] = []
            for j, resp in enumerate(responses):
                if sample.get("question_type") == "multiple-choice":
                    ext, score = mc_scores[(i, j)]
                else:
                    ext, score = oe_results[oe_idx]
                    oe_idx += 1
                cur_scores.append(score)
                cur_extract.append(ext)
            per_sample_scores.append(cur_scores)
            per_sample_extract.append(cur_extract)

    else:
        flattened: List[Tuple[Dict[str, Any], str]] = []
        sizes: List[int] = []
        for sample, responses in zip(samples, all_responses):
            sizes.append(len(responses))
            for resp in responses:
                flattened.append((sample, resp))

        print(f"  [Judge] {len(flattened)} items -> GPT ({judge_concurrency} threads)")
        judged = eval_mmvu_with_llm_judge(
            flattened,
            judge_model=judge_model,
            max_concurrency=judge_concurrency,
            rpm_limit=rpm_limit,
        )

        idx = 0
        for sz in sizes:
            sub = judged[idx : idx + sz]
            idx += sz
            per_sample_extract.append([x[0] for x in sub])
            per_sample_scores.append([x[1] for x in sub])

    final_results: List[SampleResult] = []
    for rec, scores, extracts in zip(records, per_sample_scores, per_sample_extract):
        final_results.append(
            SampleResult(
                sample=rec["sample"],
                captions=rec["captions"],
                responses=rec["responses"],
                correctness=scores,
            )
        )

    return final_results, per_sample_extract


def compute_extra_metrics(sample_results: List[SampleResult]) -> Dict[str, Any]:
    def _agg(key_name: str) -> Dict[str, Dict[str, float]]:
        grouped_bon: Dict[str, List[float]] = {}
        grouped_macc: Dict[str, List[float]] = {}
        for r in sample_results:
            key = str(r.sample.get(key_name, "Unknown"))
            grouped_bon.setdefault(key, []).append(r.bon)
            grouped_macc.setdefault(key, []).append(r.m_acc)
        return {
            "bon": {k: float(np.mean(v)) if v else 0.0 for k, v in sorted(grouped_bon.items())},
            "m_acc": {k: float(np.mean(v)) if v else 0.0 for k, v in sorted(grouped_macc.items())},
        }

    return {"question_type": _agg("question_type"), "subject": _agg("subject")}


def main() -> None:
    parser = argparse.ArgumentParser(description="Standalone MMVU Prism judge")
    parser.add_argument("--inference-path", type=str, required=True,
                        help="Inference file, e.g. mmvu_inference_step{N}.json")
    parser.add_argument("--output-dir", type=str, default=None,
                        help="Output directory; defaults to the inference file directory")
    parser.add_argument("--step", type=int, default=None,
                        help="Step id; parsed from the inference file name by default")
    parser.add_argument("--judge-mode", type=int, default=1, choices=[1, 2],
                        help="1=rule-based MC + GPT OE, 2=GPT for all questions")
    parser.add_argument("--judge-model", type=str, default="gpt-4o")
    parser.add_argument("--judge-concurrency", type=int, default=32)
    parser.add_argument("--rpm-limit", type=int, default=0,
                        help="Requests-per-minute limit. 0 disables throttling.")

    args = parser.parse_args()

    if not os.path.isfile(args.inference_path):
        raise FileNotFoundError(f"Inference file not found: {args.inference_path}")

    with open(args.inference_path, "r", encoding="utf-8") as f:
        records = json.load(f)

    if not records:
        raise ValueError("Inference file is empty")

    output_dir = args.output_dir or os.path.dirname(os.path.abspath(args.inference_path))
    os.makedirs(output_dir, exist_ok=True)

    step = args.step
    if step is None:
        m = re.search(r"step(\d+)", os.path.basename(args.inference_path))
        step = int(m.group(1)) if m else 0

    num_frames = records[0].get("sample", {}).get("num_frames")
    gen_num = len(records[0].get("captions", [])) if records else 4

    print(f"Loaded {len(records)} records from {args.inference_path}")
    print(f"Judge mode: {args.judge_mode} (1=MC rule+OE GPT, 2=all GPT)")
    print(f"Judge model: {args.judge_model}")

    final_results, extracted = evaluate_mmvu(
        records,
        judge_mode=args.judge_mode,
        judge_model=args.judge_model,
        judge_concurrency=args.judge_concurrency,
        rpm_limit=args.rpm_limit,
    )

    bon = float(np.mean([x.bon for x in final_results])) if final_results else 0.0
    m_acc = float(np.mean([x.m_acc for x in final_results])) if final_results else 0.0
    extra_metrics = compute_extra_metrics(final_results)

    details = []
    for i, x in enumerate(final_results):
        row = {
            "sample": x.sample,
            "captions": x.captions,
            "responses": x.responses,
            "correctness": x.correctness,
            "bon": x.bon,
            "m_acc": x.m_acc,
            "extracted_answers": extracted[i],
        }
        details.append(row)

    summary = {
        "benchmark": "mmvu",
        "num_samples": len(final_results),
        "num_frames": num_frames or "N/A",
        "gen_num": gen_num,
        "judge_mode": args.judge_mode,
        "judge_model": args.judge_model,
        "BoN": bon,
        "M_Acc": m_acc,
        **extra_metrics,
    }

    detail_path = os.path.join(output_dir, f"prism_mmvu_details_step{step}.json")
    summary_path = os.path.join(output_dir, f"prism_mmvu_summary_step{step}.json")
    with open(detail_path, "w", encoding="utf-8") as f:
        json.dump(details, f, ensure_ascii=False, indent=2)
    with open(summary_path, "w", encoding="utf-8") as f:
        json.dump(summary, f, ensure_ascii=False, indent=2)

    print(f"Saved details -> {detail_path}")
    print(f"Saved summary -> {summary_path}")
    print(f"Final metrics: BoN={bon:.4f}, M_Acc={m_acc:.4f}")

    if extra_metrics:
        print("\n========== Scores by subject (BoN / M_Acc) ==========")
        subject_bon = extra_metrics.get("subject", {}).get("bon", {})
        subject_macc = extra_metrics.get("subject", {}).get("m_acc", {})
        for subj in sorted(set(subject_bon) | set(subject_macc)):
            b = subject_bon.get(subj, 0.0)
            m = subject_macc.get(subj, 0.0)
            print(f"  {subj}: BoN={b:.4f}, M_Acc={m:.4f}")
        print("\n========== Scores by question_type ==========")
        qt_bon = extra_metrics.get("question_type", {}).get("bon", {})
        qt_macc = extra_metrics.get("question_type", {}).get("m_acc", {})
        for qt in sorted(set(qt_bon) | set(qt_macc)):
            b = qt_bon.get(qt, 0.0)
            m = qt_macc.get(qt, 0.0)
            print(f"  {qt}: BoN={b:.4f}, M_Acc={m:.4f}")


if __name__ == "__main__":
    main()
